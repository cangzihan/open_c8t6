void OLED_Write(unsigned char byte_add,unsigned char wdata)//按地址写入一字节数据
{
	I2C_ByteWrite(2, SSD1106_I2C_ADDR, wdata, byte_add);
}


void SSD1106_WriteCmd(u8 cmd)
{
	I2C_ByteWrite(2, (SSD1106_I2C_ADDR | 0x00), cmd, 0x00);
}

void SSD1106_WriteData(u8 data)
{
	I2C_ByteWrite(2, (SSD1106_I2C_ADDR | 0x00), data, 0x40);
}

void OLED_SetPos(u8 x, u8 y)
{
	SSD1106_WriteCmd(0xb0+y);
	SSD1106_WriteCmd(((x&0xf0)>>4)|0x10);
	SSD1106_WriteCmd((x&0x0f));
}

void OLED_DisplayOn(void)
{
	SSD1106_WriteCmd(0x8D);
	SSD1106_WriteCmd(0x14);
	SSD1106_WriteCmd(0xAF);
}

void OLED_DisplayOff(void)
{
	SSD1106_WriteCmd(0x8D);
	SSD1106_WriteCmd(0x10);
	SSD1106_WriteCmd(0xAE);
}

